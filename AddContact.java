    private void SavebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SavebtnActionPerformed

    String fName = firstNameField.getText().trim();
    String lName = LastNameField.getText().trim();
    String phone = PhoneNofield.getText().trim();
    String email = Emailfield.getText().trim();
    String address = AddressField.getText().trim();
    String selectedGroup = GroupComboBox.getSelectedItem().toString();

    
    if (fName.isEmpty() || phone.isEmpty()) {
        JOptionPane.showMessageDialog(this, "First Name and Phone Number cannot be empty.", "Validation Error", JOptionPane.ERROR_MESSAGE);
        return; 
    }

    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/phonebook", "Ayesha", "password123");

        String contactQuery = "INSERT INTO contacts (first_name, last_name, phone_number, email, address) VALUES (?, ?, ?, ?, ?)";
        PreparedStatement contactStmt = conn.prepareStatement(contactQuery, Statement.RETURN_GENERATED_KEYS);
        contactStmt.setString(1, fName);
        contactStmt.setString(2, lName);
        contactStmt.setString(3, phone);
        contactStmt.setString(4, email);
        contactStmt.setString(5, address);
        contactStmt.executeUpdate();

        ResultSet contactKeys = contactStmt.getGeneratedKeys();
        int contactId = 0;
        if (contactKeys.next()) {
            contactId = contactKeys.getInt(1);
        }
        contactKeys.close();
        contactStmt.close();

         if (!"No Group".equals(selectedGroup)) {
            String groupQuery = "SELECT id FROM groupps WHERE name = ?";
            PreparedStatement groupStmt = conn.prepareStatement(groupQuery);
            groupStmt.setString(1, selectedGroup);
            ResultSet groupRs = groupStmt.executeQuery();

            if (groupRs.next()) {
                int groupId = groupRs.getInt("id");

                String contactGroupQuery = "INSERT INTO contact_groups (contact_id, group_id) VALUES (?, ?)";
                PreparedStatement contactGroupStmt = conn.prepareStatement(contactGroupQuery);
                contactGroupStmt.setInt(1, contactId);
                contactGroupStmt.setInt(2, groupId);
                contactGroupStmt.executeUpdate();
                contactGroupStmt.close();
            }
            groupRs.close();
            groupStmt.close();
        }

        JOptionPane.showMessageDialog(this, "Contact added successfully!");
        conn.close();

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error saving contact: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_SavebtnActionPerformed

