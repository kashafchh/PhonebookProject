   private void populateGroupComboBox(JComboBox<String> groupComboBox) {
    String query = "SELECT name FROM groupps";

    try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/phonebook", "Ayesha", "password123");
         PreparedStatement stmt = conn.prepareStatement(query);
         ResultSet rs = stmt.executeQuery()) {
          groupComboBox.addItem("No Group");  
        while (rs.next()) {
            String groupName = rs.getString("name");
            groupComboBox.addItem(groupName);
        }
    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading groups: " + ex.getMessage());
    }
}


